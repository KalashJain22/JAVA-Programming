public class Arithmetic {
    public static void main(String[] args) {
        int a = 4;
        int b = 3;
        
        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b);
        System.out.println(a%b);

        double x = 8;
        double y = 7;
        
        System.out.println(x+y);
        System.out.println(x-y);
        System.out.println(x*y);
        System.out.println(x/y); 
    }
}
